export function generateSessionId(employeeId: string): string {
  return `sess_${employeeId}_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;
}

export function generateAuthToken(employeeName: string): string {
  const nameHash = employeeName.replace(/\s+/g, '').toLowerCase();
  return `auth_${nameHash}_${Date.now()}_${Math.random().toString(36).substr(2, 12)}`;
}

